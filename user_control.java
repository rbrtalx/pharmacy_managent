    public ArrayList<customers> userList_1(){
        ArrayList<customers> usersList = new ArrayList<>();
        PreparedStatement ps;
        String query1 = "SELECT * FROM customer";
        try {
            ps = myconnection.getConnection().prepareStatement(query1);
            ResultSet rs = ps.executeQuery(query1);
            customers user;
            while(rs.next()){
                user = new customers(rs.getString("cust_name"),rs.getString("medicine_name"),rs.getString("quantity"),rs.getString("date_info"),rs.getString("price"));
                usersList.add(user);
            }
        } catch (SQLException ex) {
            Logger.getLogger(user_control.class.getName()).log(Level.SEVERE, null, ex);
        }
        return usersList;
    }
    public void show_info(){
        ArrayList<customers> list = userList_1();
        DefaultTableModel model = (DefaultTableModel)datatable.getModel();
        Object[] row = new Object[5];
        for(int i = 0;i < list.size();i++){
            row[0] = list.get(i).get_customer_name();
            row[1] = list.get(i).get_medicine_name();
            row[2] = list.get(i).get_quantity();
            row[3] = list.get(i).get_date();
            row[4] = list.get(i).get_price();
            model.addRow(row);
        }
    }
    public void medicine_price(){
        PreparedStatement ps;
        ResultSet rs;
        String mname = (String) medicine_name.getSelectedItem();
        int qnty = Integer.parseInt(quantity.getText());
        String query = "SELECT `price` FROM `medicine_information` WHERE `med_name` =?";
        try {
            ps  = myconnection.getConnection().prepareStatement(query);
            ps.setString(1, mname);
            rs = ps.executeQuery();
            if(rs.next()){
                int price = rs.getInt("price");
                int total_price = price * qnty;
                t_price.setText(String.valueOf(total_price));
            }
        } catch (SQLException ex) {
            Logger.getLogger(user_control.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Can't fetch!!");
        }
    }
