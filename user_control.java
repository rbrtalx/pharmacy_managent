    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String customer_name = cust_name.getText();
        String med_name = (String) medicine_name.getSelectedItem();
        String qnty = quantity.getText();
        String date = date_choose.getText();
        String price = t_price.getText();
        String query = "INSERT INTO `customer`(`cust_name`, `medicine_name`, `quantity`, `date_info`, `price`) VALUES (?,?,?,?,?)";
        PreparedStatement ps;
        if(customer_name.equals("")){
            JOptionPane.showMessageDialog(null,"Enter Customer Name FIrst!!");
        }
        else if(qnty.equals("")){
            JOptionPane.showMessageDialog(null,"Enter Quantity First!!");
        }
        else if(price.equals("")){
            JOptionPane.showMessageDialog(null,"Press Count Button First");
        }
        else{
                try {
                ps = myconnection.getConnection().prepareStatement(query);
                ps.setString(1, customer_name);
                ps.setString(2, med_name);
                ps.setString(3, qnty);
                ps.setString(4, date);
                ps.setString(5, price);
                if(ps.executeUpdate() > 0){
                    JOptionPane.showMessageDialog(null, "Information Added Successfully!!!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(user_control.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

