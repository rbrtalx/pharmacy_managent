    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        PreparedStatement ps;
        ResultSet rs;
        String mname = (String) medicine_name.getSelectedItem();
        int qnty = Integer.parseInt(quantity.getText());
        int total_medicine;
        String query1 = "SELECT `med_quantity` FROM `medicine_information` WHERE `med_name` =?";
        String query2;
        try {
            ps  = myconnection.getConnection().prepareStatement(query1);
            ps.setString(1, mname);
            rs = ps.executeQuery();
            if(rs.next()){
                int number_of_med = rs.getInt("med_quantity");
                total_medicine = number_of_med - qnty;
                if(total_medicine > 0){
                    query2 = "Update medicine_information set med_quantity = ? where med_name = ?";
                    ps = myconnection.getConnection().prepareStatement(query2);
                    ps.setInt(1,total_medicine);
                    ps.setString(2, mname);
                    if(ps.executeUpdate() > 0){
                        JOptionPane.showMessageDialog(null, "Medicine Quantity Updated Successfully");
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null,"Medicine Out Off Stock!!!");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(user_control.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Can't fetch!!");
        }
    }//GEN-LAST:event_updateActionPerformed

